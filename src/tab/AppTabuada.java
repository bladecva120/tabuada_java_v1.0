    private void bntSairActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntSairActionPerformed
        // TODO add your handling code here:
        int resp = JOptionPane.showConfirmDialog(null, "Deseja realmente sair? ");
        switch (resp) {
            case 0:
                System.exit(0);
            case 1:
                campoNum.requestFocus();
                display.setText("");
                campoNum.setText(null);
                combo.setEnabled(false);
                display.setEnabled(false);

            case 2:
                campoNum.requestFocus();
                display.setText("");
                campoNum.setText(null);
                combo.setEnabled(false);
                display.setEnabled(false);
        }
    }//GEN-LAST:event_bntSairActionPerformed

