    double res, num2;
    private void comboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboActionPerformed
        // TODO add your handling code here:
        double num1 = Double.parseDouble(campoNum.getText());
        int indice = combo.getSelectedIndex();

        //TABUADA DE ADIÇÃO
        if (indice == 0) {
            if (num1 < 10) {
                num2 = num1 + (10 - num1);
                for (int i = 0; i <= num2; i++) {
                    res = i + num1;
                    display.append(i + " + " + (int) num1 + " = " + (int) res + "\n");
                }
            } else {
                for (int i = 0; i <= num1; i++) {
                    res = i + num1;
                    display.append(i + " + " + (int) num1 + " = " + (int) res + "\n");
                }
            }
        }

        // TABUADA DE SUBTRAÇÃO
        if (indice == 1) {
            if (num1 < 10) {
                num2 = num1 + (10 - num1);
                for (int i = (int) num1; i <= (num2 + num1); i++) {
                    res = i - num1;
                    display.append(i + " - " + (int) num1 + " = " + (int) res + "\n");
                }
            } else {
                for (int i = (int) num1; i <= num1 + 10; i++) {
                    res = i - num1;
                    display.append(i + " - " + (int) num1 + " = " + (int) res + "\n");
                }
            }
        }

        // TABUADA DE MULTIPLICAÇÃO
        if (indice == 2) {
            if (num1 < 10) {
                num2 = num1 + (10 - num1);
                for (int i = 0; i <= num2; i++) {
                    res = i * num1;
                    display.append((int) num1 + " × " + i + " = " + (int) res + "\n");
                }
            } else {
                for (int i = 0; i <= num1; i++) {
                    res = i * num1;
                    display.append((int) num1 + " × " + i + " = " + (int) res + "\n");
                }
            }
        }

        // TABUADA DE DIVISÃO
        if (indice == 3) {
            if (num1 < 10) {
                num2 = num1 + (10 - num1);
                for (int i = (int) num1; i <= num2 + num1; i++) {
                    res = i / num1;
                    display.append(i + " ÷ " + (int) num1 + " = " + String.valueOf(res).format("%.3f", res) + "\n");
                }
            } else {
                for (int i = (int) num1; i <= num1 + 10; i++) {
                    res = i / num1;
                    display.append(i + " ÷ " + (int) num1 + " = " + String.valueOf(res).format("%.3f", res) + "\n");
                }
            }
        }
    }//GEN-LAST:event_comboActionPerformed

